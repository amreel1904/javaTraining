package day6.Array;

public class arrayOnConsole {
    public static void main(String[] args) {
        String[]arr={"one","two","tiga","empat","lima","enam","tujuh","lapan","sembilan","sepuluh"};

        for(int i=0; i<arr.length;i++){
            System.out.printf("arr[%d] = %s%n", i, arr[i]);
        }
    }
}
